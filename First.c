#include <stdio.h>
#include <stdlib.h>
// declaration,defination and initialization
int main()
{
  // declaratin with defination
  int defined_var;
  printf("Defined_var: %d\n", defined_var);

  // initialization
  defined_var = 12;
  // def+dec+ini
  int ini_var = 126;

  printf("Value of defined_var after intializATation: %d\n", defined_var);
  printf("value of ini_var: %d", ini_var);

  return 0;
}
